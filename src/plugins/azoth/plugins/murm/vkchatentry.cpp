	void VkChatEntry::InviteToMUC (const QString& userId, const QString&)
	{
		bool ok = false;
		const auto numericId = userId.toULongLong (&ok);

		if (!ok)
		{
			qWarning () << Q_FUNC_INFO
					<< "incorrect user id"
					<< userId;
			return;
		}

		Account_->GetConnection ()->AddChatUser (Info_.ChatID_, numericId);
	}
