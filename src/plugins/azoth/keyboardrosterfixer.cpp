#include "keyboardrosterfixer.h"
#include <QKeyEvent>
#include <QApplication>
#include <QTreeView>
#include <QLineEdit>

namespace LeechCraft
{
namespace Azoth
{
	KeyboardRosterFixer::KeyboardRosterFixer (QLineEdit *edit, QTreeView *view, QObject *parent)
	: QObject (parent)
	, Edit_ (edit)
	, View_ (view)
	, IsSearching_ (false)
	{
		Edit_->installEventFilter (this);
	}

	bool KeyboardRosterFixer::eventFilter (QObject*, QEvent *e)
	{
		if (e->type () != QEvent::KeyPress &&
			e->type () != QEvent::KeyRelease)
			return false;

		if (IsSearching_ &&
				Edit_->text ().isEmpty ())
			IsSearching_ = false;

		QKeyEvent *ke = static_cast<QKeyEvent*> (e);
		if (!IsSearching_)
		{
			switch (ke->key ())
			{
			case Qt::Key_Space:
			case Qt::Key_Right:
			case Qt::Key_Left:
				qApp->sendEvent (View_, e);
				return true;
			default:
				;
			}
		}

		switch (ke->key ())
		{
		case Qt::Key_Down:
		case Qt::Key_Up:
		case Qt::Key_PageDown:
		case Qt::Key_PageUp:
		case Qt::Key_Enter:
		case Qt::Key_Return:
		case Qt::Key_Escape:
			IsSearching_ = false;
			qApp->sendEvent (View_, e);
			return true;
		default:
			IsSearching_ = true;
			return false;
		}
	}
}
}
